def afk_case_coins(case_key, rank_idx):
    mult = 1 + rank_idx * 0.18
    ranges = {
        "common": (90, 180),
        "rare": (220, 500),
        "epic": (550, 1200),
        "legendary": (1300, 2800),
        "mythic": (3000, 6500),
    }
    lo, hi = ranges.get(case_key, (1, 2))
    return int(random.randint(lo, hi) * mult)


