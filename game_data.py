def get_enchant_shop_pool() -> list[tuple[str, int]]:
    """Возвращает список из ENCHANT_SHOP_DAILY_COUNT случайных (enchant_key, level) пар."""
    keys = list(ENCHANT_CATALOG.keys())
    result = []
    for _ in range(ENCHANT_SHOP_DAILY_COUNT):
        key = random.choice(keys)
        lvl = random.choices([1, 2, 3], weights=ENCHANT_SHOP_LEVEL_WEIGHTS, k=1)[0]
        result.append((key, lvl))
    return result


def dungeon_mode_for_day(weekday):
    mapping = {
        0: "tomb",
        1: "grot",
        2: "greed",
        3: "tomb",
        4: "grot",
        5: "chaos",
        6: "chaos",
    }
    return mapping.get(weekday, "chaos")


def random_shard():
    return random.choices([1, 2, 3, 4, 5], weights=SHARD_WEIGHTS, k=1)[0]


def wave_gold(wave, arena, mode):
    base = int((32 + wave * 8 + arena * 22) * (1 + wave * 0.025))
    if mode == "greed":
        base *= 2
    return base


def enemy_hp(wave, arena):
    return int(1800 + (wave ** 2.25) * 180 + arena * 2200)


def enemy_atk(wave, arena):
    return int(40 + wave * 14 + arena * 26)