DROP_WEIGHTS = [90, 9, 1]


def _roll_from_table(arena, table):
    tiers = table.get(max(1, min(arena, max_arena())), table[1])
    if len(tiers) == 1:
        lo, hi = tiers[0]
        return random.randint(lo, hi)
    if len(tiers) == 2:
        weights = [95, 5]
    else:
        weights = DROP_WEIGHTS
    idx = random.choices(range(len(tiers)), weights=weights, k=1)[0]
    lo, hi = tiers[idx]
    return random.randint(lo, hi)


def get_weapon_roll(arena):
    return _roll_from_table(arena, WEAPON_ROLLS)


def get_pet_roll(arena):
    arena = max(1, min(arena, max_arena()))
    if arena in PET_CASE_POOLS:
        pool = PET_CASE_POOLS[arena]
        idx = random.choices(range(len(pool)), weights=[it[3] for it in pool], k=1)[0]
        _, lo, hi, _ = pool[idx]
        return random.randint(lo, hi)
    return _roll_from_table(arena, PET_ROLLS)


