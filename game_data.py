def get_weapon_name(bonus):
    out = WEAPON_NAME_TABLE[0][1]
    # Всегда идем по порогам по возрастанию, чтобы имя соответствовало реальному bonus.
    for edge, name in sorted(WEAPON_NAME_TABLE, key=lambda x: x[0]):
        if bonus >= edge:
            out = name
    return out


def get_pet_name(bonus, arena=None):
    if arena in PET_CASE_POOLS:
        out = PET_CASE_POOLS[arena][0][0]
        for name, lo, hi, _ in PET_CASE_POOLS[arena]:
            if lo <= bonus <= hi:
                return name
            if bonus >= lo:
                out = name
        return out

    out = PET_NAME_TABLE[0][1]
    for edge, name in sorted(PET_NAME_TABLE, key=lambda x: x[0]):
        if bonus >= edge:
            out = name
    return out


